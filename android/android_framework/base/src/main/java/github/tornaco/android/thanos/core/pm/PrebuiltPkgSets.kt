const val PREBUILT_PACKAGE_SET_ID_WHITELISTED = "E686ED30-C1A4-4D49-8BFF-8E4D7B4B1CF0"
const val PREBUILT_PACKAGE_SET_ID_DISABLED = "E8EB535E-A0D9-42D9-A73F-496B1EB0B9E7"
const val PREBUILT_PACKAGE_SET_ID_ALL = "C60FC34B-DC18-4087-845F-1D6AAE118D16"

const val USER_PACKAGE_SET_ID_USER_WHITELISTED = "6E132918-0B02-43DA-8B33-A2BB62643EF2"

object PrebuiltPkgSets {
    fun String.toAppFlags(): Int {
        return when (this) {
            PREBUILT_PACKAGE_SET_ID_SYSTEM -> AppInfo.FLAGS_SYSTEM or AppInfo.FLAGS_SYSTEM_UID or AppInfo.FLAGS_SYSTEM_MEDIA or AppInfo.FLAGS_SYSTEM_PHONE or AppInfo.FLAGS_WEB_VIEW_PROVIDER
            PREBUILT_PACKAGE_SET_ID_3RD -> AppInfo.FLAGS_USER
            PREBUILT_PACKAGE_SET_ID_SHORTCUT -> AppInfo.FLAGS_SHORTCUT_PROXY
            PREBUILT_PACKAGE_SET_ID_WHITELISTED -> AppInfo.FLAGS_WHITE_LISTED
            PREBUILT_PACKAGE_SET_ID_DISABLED -> AppInfo.FLAGS_DISABLED
            PREBUILT_PACKAGE_SET_ID_ALL -> AppInfo.FLAGS_ALL
            else -> AppInfo.FLAGS_NONE
        }
    }
}
